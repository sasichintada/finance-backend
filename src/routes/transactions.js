const express = require('express');
const router = express.Router();
const transactionController = require('../controllers/transactionController');
const { authenticate, authorize, checkUserStatus } = require('../middleware/auth');
const { transactionValidation } = require('../middleware/validation');

router.use(authenticate);
router.use(checkUserStatus);

router.post('/', 
  authorize('canCreateTransaction'),
  transactionValidation.create, 
  transactionController.createTransaction
);

router.get('/', 
  authorize('canViewTransactions'),
  transactionValidation.filters,
  transactionController.getTransactions
);

router.get('/:id', 
  authorize('canViewTransactions'),
  transactionController.getTransactionById
);

router.put('/:id', 
  authorize('canUpdateTransaction'),
  transactionValidation.update,
  transactionController.updateTransaction
);

router.delete('/:id', 
  authorize('canDeleteTransaction'),
  transactionController.deleteTransaction
);

module.exports = router;